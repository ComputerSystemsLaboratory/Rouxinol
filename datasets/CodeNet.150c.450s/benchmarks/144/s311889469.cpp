#include <iostream>
#include <sstream>
#include <vector>

int main(int argc, char const *argv[]) {
  unsigned short n, m, l;
  std::string Param;
  std::istringstream iss;
  while (true) {

    getline(std::cin, Param);
    iss.str(Param);
    iss >> n >> m >> l;
    iss.clear();
    if (n > 100 || m > 100 || l > 100) {
      std::cout << "Error: 1 <= n, m, l <= 100" << std::endl;
    } else {
      break;
    }
  }

  std::vector<std::vector<unsigned short>> A(n,
                                             std::vector<unsigned short>(m, 0));
  std::vector<std::vector<unsigned short>> B(m,
                                             std::vector<unsigned short>(l, 0));
  std::vector<std::vector<unsigned long long>> C(n,
                                             std::vector<unsigned long long>(l, 0));

  unsigned short i = 0;
  bool isValid = true;

  while (i < n) {
    getline(std::cin, Param);
    iss.str(Param);
    for (size_t j = 0; j < m; j++) {
      iss >> A[i][j];
      if (A[i][j] > 10000) {
        std::cout << "Error: 0 <= ai <= 10000" << std::endl;
        isValid = false;
        break;
      } else {
        isValid = true;
      }
    }
    iss.clear();
    if (isValid) {
      i++;
    }
  }

  i = 0;
  while (i < m) {
    getline(std::cin, Param);
    iss.str(Param);
    for (size_t j = 0; j < l; j++) {
      iss >> B[i][j];
      if (B[i][j] > 10000) {
        std::cout << "Error: 0 <= bi <= 10000" << std::endl;
        isValid = false;
        break;
      } else {
        isValid = true;
      }
    }
    iss.clear();
    if (isValid) {
      i++;
    }
  }
  for (size_t j = 0; j < l; j++) {
    for (size_t i = 0; i < n; i++) {
      for (size_t k = 0; k < m; k++) {
        C[i][j] += A[i][k] * B[k][j];
      }
    }
  }
  for (const auto &Row : C) {
    for (size_t i = 0; i < l; i++) {
      std::cout << Row[i];
      if (i < l - 1) {
        std::cout << " ";
      }
    }
    std::cout << std::endl;
  }
  return 0;
}