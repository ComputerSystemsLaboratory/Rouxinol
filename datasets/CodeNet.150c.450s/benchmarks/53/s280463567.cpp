#include <bits/stdc++.h>
#define eps 1e-2
using namespace std;

int main()
{
	int n, nn;
	vector<int> res;
	cin >> n;
	nn = n;
	for (int i = 2; i <= sqrt(n) + eps; i++) {
		while (nn % i == 0) {
			res.push_back(i);
			nn /= i;
		}
	}
	if (nn != 1)res.push_back(nn);
	cout << n << ':';
	for (int i = 0; i < res.size(); i++) {
		cout << ' ' << res[i];
	}
	cout << endl;
	return 0;
}