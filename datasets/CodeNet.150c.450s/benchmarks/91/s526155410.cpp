#include <iostream>
using namespace std;

#define Max 1000000
int Prime[Max] = {};

int main()
{
	//?????????
	for (int i = 2; i < Max; i++)
	{
		Prime[i] = 1;
	}
	//??¨??????????????????????????????
	for (int i = 2; i*i < Max; i++)
	{
		if (Prime[i] == 1)
		{
			for (int j = i * 2; j < Max; j += i)
			{
				Prime[j] = 0;
			}
		}
	}
	//?´???????????¨????
	for (int i = 2; i < Max; i++)
	{
		Prime[i] += Prime[i - 1];
	}

	int n;
	while (cin >> n)
	{
		cout << Prime[n] << endl;
	}

	return 0;
}