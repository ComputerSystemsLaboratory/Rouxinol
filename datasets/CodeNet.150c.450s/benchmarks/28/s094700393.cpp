int C_num(int P,int k,vector<int> w,int n){
  int i=0,cnt=0,sum,j=0;
  while(i<k){
    sum=0;
    while(sum+w[j]<=P){
      sum+=w[j];
      j++;
      if(j==n) return n;
    }
    i++;
  }
  return cnt;
}

int solve(int k,vector<int> w,int n){
  int left=0;
  int right=10000*100000; //?????§?????????????????§??????
  int mid;
  while(right-left>1){
    mid=(right+left)/2;
    int v=C_num(mid,k,w,n);
    if(v>=n) right=mid;
    else left = mid;
  }
  return right;
}

int main(){
  vector<int> w;
  int n,k,temp;
  cin >> n >> k;
  for(int i=0;i<n;i++) {
    cin >> temp;
    w.push_back(temp);
  }
  int ans=solve(k,w,n);
  cout << ans << endl;
  return 0;
}