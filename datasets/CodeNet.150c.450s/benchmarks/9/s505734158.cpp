string shuffle(string &str, int h)//?????????h?????????????????????????????£?????????
{
	string copy_str = str;
	
	for(int i = 0; i < h; i++){
		copy_str.push_back(str[i]);
	}
	
	for(int i = 0; i < str.size(); i++){
		str[i] = copy_str[i + h];
	}
	return str;
}
