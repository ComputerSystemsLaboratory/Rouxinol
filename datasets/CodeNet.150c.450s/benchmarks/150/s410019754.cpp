int n, A[2000005], B[2000005];

void CountingSort(int A[], int B[], int k) {
	int C[2000005];
	for (int i = 0;i <= k;i++) C[i] = 0;

	//??????????????§O(n)??§i??\??????????????°?????????????????????????´???§???????????¨?????????
	//C[i]???i???????????°????¨????
	for (int j = 1;j <= n;j++) C[A[j]]++;

	//C[i]???i??\????????°???????????°????¨????(??°?´????:C[n]=n+C[n-1]=n+(n-1)+C[n-2]=...)
	for (int i = 1;i <= k;i++) C[i] = C[i] + C[i - 1];
	//???????????§

	for (int j = n ;j > 0;j--) {
		B[C[A[j]]] = A[j];
		C[A[j]]--;
	}

}

