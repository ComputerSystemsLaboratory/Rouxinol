int main() {
    int e;
    while (cin >> e, e) {
        int m = 1000000;
        for (int y = 0; y*y <= e; y++)
        for (int z = 0; y*y + z*z*z <= e; z++) {
            int x = e - y*y - z*z*z;
            if (m > x + y + z) {
                m = x + y + z;
            }
        }
        cout << m << endl;
    }
}