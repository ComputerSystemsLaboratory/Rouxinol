#include <iostream>
#include <limits>
#include <vector>
using namespace std;

int main() {
	int n;
	vector<int> r;

	cin >> n;
	for(int i = 0; i < n; ++i) {
		int tmp;
		cin >> tmp;
		r.push_back(tmp);
	} // O(n)

	// ????°???§????????????????????????i??¨????????????r[i]???????´?
	int max = numeric_limits<int>::min();
	int min = r[0];
	for(int i = 1; i < n; ++i) {
		max = (max >= r[i]-min)?max:(r[i]-min);
		min = (min <= r[i])?min:(r[i]);
	}
	cout << max << endl;

	return 0;
}