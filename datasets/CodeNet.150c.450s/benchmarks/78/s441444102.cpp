#include <iostream>
using namespace std;

int dp[1000001];
int dpOdd[1000001];

int main() {
	int i, j;
	
	for (i = 0; i < 1000001; i++) dp[i] = 1145141919;
	for (i = 0; i < 1000001; i++) dpOdd[i] = 1145141919;
	dp[0] = 0;
	dpOdd[0] = 0;
	
	for (i = 0; i < 1000001; i++) {
		for (j = 1; i + j * (j + 1) * (j + 2) / 6 < 1000001; j++) {
			int num = j * (j + 1) * (j + 2) / 6;
			dp[i + num] = min(dp[i + num], dp[i] + 1);
			if (num % 2 == 1) {
				dpOdd[i + num] = min(dpOdd[i + num], dpOdd[i] + 1);
			}
		}
	}
	
	int n;
	while (cin >> n) {
		if (n == 0) break;
		cout << dp[n] << " " << dpOdd[n] << endl;
	}
	return 0;
}