#include <stdio.h>
#include <string.h>
#include <iostream>
#include <algorithm>

using namespace std;

int main(int argc, char* argv[])
{
	int i, j;
	int n, m;	// 1 <= n, m <= 100
	int a[101][101];
	int b[101];
	int answer[101];

	memset(answer, 0, sizeof(answer));

	cin >> n >> m;

	for (i = 1; i <= n; i++) {
		for (j = 1; j <= m; j++) {
			cin >> a[i][j];
		}
	}
	for (i = 1; i <= m; i++) {
		cin >> b[i];
	}

	for (i = 1; i <= n; i++) {
		for (j = 1; j <= m; j++) {
			answer[i] += a[i][j] * b[j];
		}
	}

	for (i = 1; i <= n; i++) {
		cout << answer[i] << endl;
	}

	return 0;
}