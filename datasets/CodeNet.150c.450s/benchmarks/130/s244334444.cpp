#include <iostream>
#include <sstream>
#include <vector>

int main(int argc, char const *argv[]) {
  unsigned short m, n;
std::string Param;
std::istringstream iss;
  while (true) {


  getline(std::cin, Param);
  iss.str(Param);
  iss >> n >> m;
iss.clear();
  if (n > 100 || m > 100) {
    std::cout << "Error: 0 < m, n < 100" << std::endl;
  } else {
    break;
  }
}

std::vector<std::vector<unsigned short>> Matrix(n, std::vector<unsigned short>(m, 0));

for (size_t i = 0; i < n; i++) {
  getline(std::cin, Param);
  iss.str(Param);
  for (size_t j = 0; j < m; j++) {
    iss >> Matrix[i][j];
  }
  iss.clear();
}

std::vector<unsigned short> Vector(m, 0);
for (size_t i = 0; i < m; i++) {
  getline(std::cin, Param);
  iss.str(Param);
  iss >> Vector[i];
  iss.clear();
}
unsigned int p = 0;
for (size_t i = 0; i < n; i++) {
  p = 0;
  for (size_t j = 0; j < m; j++) {
    p += Matrix[i][j] * Vector[j];
  }
  std::cout << p << std::endl;
}
  return 0;
}