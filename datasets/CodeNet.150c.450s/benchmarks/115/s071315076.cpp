#include <iostream>
#include <algorithm>
#include <string>
using namespace std;

int N,len1,len2;
string X,Y;
int Z[1010][1010];

int main() {
  cin >> N;
  for (int i=0; i<N; i++){
    cin >> X >> Y;
    len1 = X.length();
    len2 = Y.length();
    for (int i=1; i<=len1; i++){
      for (int j=1; j<=len2; j++){
       if (X[i-1] == Y[j-1]){
         Z[i][j] = Z[i-1][j-1] + 1;
       }
       else if (Z[i][j-1] > Z[i-1][j]){
         Z[i][j] = Z[i][j-1];
       }
       else{
         Z[i][j] = Z[i-1][j];
       }
     }
   }
   cout << Z[len1][len2] << endl;
 }
}