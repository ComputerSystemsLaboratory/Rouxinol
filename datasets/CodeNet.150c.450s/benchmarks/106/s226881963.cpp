#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  unsigned int a, b, c;
  a = b = c = 1;

  while (true) {
    std::string param;

    getline(std::cin, param);
    std::istringstream iss(param);
    iss >> a >> b >> c;

    if ((a < 1 || a > 10000) || (b < 1 || b > 10000) || (c < 1 || c > 10000)) {
      std::cout << "Error: 1 <= a, b, c <= 10000" << std::endl;
    } else {
      break;
    }
  }

  unsigned int count = 0;
  for (size_t i = a; i <= b; i++) {
    if (c % i == 0) {
      count++;
    }
  }
  std::cout << count << std::endl;
  
  return 0;
}