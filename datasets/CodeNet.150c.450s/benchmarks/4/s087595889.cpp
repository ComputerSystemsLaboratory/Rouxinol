#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  short m, f, r;
  std::string Result;

  while (true) {
    while (true) {
      std::string Param;

      getline(std::cin, Param);
      std::istringstream iss(Param);
      iss >> m >> f >> r;

      if (m < -1 || m > 50 || f < -1 || f > 50 || r < -1 || r > 100) {
        std::cout << "Error: m, f <= 50, r <= 100" << std::endl;
      } else {
        break;
      }
    }
    if (m == -1 && f == -1 && r == -1) {
      break;
    } else {
      short Sum = m + f;
      if (m == -1 || f == -1) {
        Result = "F";
      } else if (Sum >= 80) {
        Result = "A";
      } else if (Sum >= 65) {
        Result = "B";
      } else if (Sum >= 50 || Sum + r >= 80) {
        Result = "C";
      } else if (Sum >= 30) {
        Result = "D";
      } else if (Sum < 30) {
        Result = "F";
      }
      std::cout << Result << std::endl;
    }
  }
  return 0;
}