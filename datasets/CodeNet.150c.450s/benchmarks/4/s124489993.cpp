#include <stdio.h>

int main(void)
{
	int m, f, r;//??????????¨?????????°, ??????????¨?????????°, ???????¨?????????°
	
	while(true){
		
		scanf("%d %d %d", &m, &f, &r);
	
		if(m == -1 && f == -1 && r == -1){
			break;
		}
		
		if(m == -1 || f == -1 || m + f < 30){
			printf("F\n");
		}
		else if(m + f >= 80){
			printf("A\n");
		}
		else if(65 <= m + f){
			printf("B\n");
		}
		else if(50 <= m + f){
			printf("C\n");
		}
		else if(30 <= m + f){
			if(r >= 50){
				printf("C\n");
			}
			else{
				printf("D\n");
			}
		}
		
	}
}