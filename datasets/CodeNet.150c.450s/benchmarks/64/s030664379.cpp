bool existsCombination(int* A, int sum, int left){
	// left??°?????????????´?????????????????´??????????????????????????¨?sum?????§?????????
	if(left >= n) return false; 
	if(sum == A[left]) return true;
	else
		return existsCombination(A, sum, left+1) || existsCombination(A, sum-A[left], left+1);

}


int main(){

	cin >> n;
	int A[n];
	for(int i=0;i<n;i++) cin >> A[i];		

	int q;
	cin >> q;
	int m[q];
	for(int i=0; i<q; i++) cin >> m[i];

	for(int i=0; i<q; i++){
		if(existsCombination(A, m[i], 0))
			cout << "yes" << endl;
		else
			cout << "no" << endl;
	}

	return 0;
}