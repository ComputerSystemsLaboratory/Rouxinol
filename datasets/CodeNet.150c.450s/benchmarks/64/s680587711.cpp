{
	if(m==0){
		return true;
	}
	if(i >= (A.size())){
		return false;
	}
	bool res ;
	bool res1 = solve( (i+1), m, A);
	bool res2 = solve((i+1), m- A[i], A);
	if(res1 || res2){
		res = true;
	}
	else{
		res = false;
	}
	
	return res;
}
