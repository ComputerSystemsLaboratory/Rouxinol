#include <iostream>
#include <sstream>
#include <vector>

bool solve(const std::vector<unsigned short> &A, unsigned short i,
           unsigned short mi) {
  const unsigned short n = A.size();
  if (mi == 0) {
    return true;
  }
  if (i >= n) {
    return false;
  }
  bool Result = solve(A, i + 1, mi) || solve(A, i + 1, mi - A[i]);
  return Result;
}

int main(int argc, char const *argv[]) {
  std::string Param;
  std::istringstream Stream;
  unsigned short n = 0;
  unsigned short q = 0;
  std::vector<unsigned short> A;
  std::vector<unsigned short> m;

  getline(std::cin, Param);
  Stream.str(Param);
  Stream >> n;
  Stream.clear();

  getline(std::cin, Param);
  Stream.str(Param);
  for (size_t i = 0; i < n; i++) {
    unsigned short Ai = 0;
    Stream >> Ai;
    A.push_back(Ai);
  }
  Stream.clear();

  getline(std::cin, Param);
  Stream.str(Param);
  Stream >> q;
  Stream.clear();

  getline(std::cin, Param);
  Stream.str(Param);
  for (size_t i = 0; i < q; i++) {
    unsigned short mi = 0;
    Stream >> mi;
    m.push_back(mi);
  }
  Stream.clear();

  bool Result;

  for (const auto &mi : m) {
    Result = solve(A, 0, mi);
    if (Result) {
      std::cout << "yes" << std::endl;
    } else {
      std::cout << "no" << std::endl;
    }
  }

  return 0;
}