string henkan(string inp)
{	
	int len = inp.size(); //= inp.length();
	
	for(int i = 0; i < len; i++){
		if(inp[i] >= 'A' && inp[i] <= 'Z'){
			inp[i] += 32;
		}
		else if(inp[i] >= 'a' && inp[i] <= 'z'){
			inp[i] -= 32;
		}
	}
	return inp;
}