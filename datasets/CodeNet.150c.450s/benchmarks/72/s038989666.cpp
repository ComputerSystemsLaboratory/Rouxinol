#include <iostream>
#include <cctype>
#include <cstdio>

int main() {
  char ch;
  char line[1200];

  /*                                                                            
    ????????\???????????????????????????????????????                                            
    cin?????¨???getline()???????????????????????????                                        
    scanf?????¨???????????????????????§?????????????????????                                     
   */

  for (int i = 0;i < 1200;i++) {
    scanf("%c", &ch);
    if (ch == '\n') {
      line[i] = '\0';
      break;
    }
    if (isupper(ch)) {
      line[i] = (char)tolower(ch);
    } else if (islower(ch)) {
      line[i] = (char)toupper(ch);
    } else {
      line[i] = ch;
    }
  }

  std::cout << line << std::endl;
  return 0;
}