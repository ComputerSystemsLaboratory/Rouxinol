#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  int a, b;
  a = b = 0;

  while (true) {
    std::string param;

    getline(std::cin, param);
    std::istringstream iss(param);
    iss >> a >> b;

    if ((a < -1000 || a > 1000) || (b < -1000 || b > 1000)) {
      std::cout << "Error: -1000 <= a, b <= 1000" << std::endl;
    } else {
      break;
    }
  }
  if (a < b) {
    std::cout << "a < b" << std::endl;
  } else if (a > b) {
    std::cout << "a > b" << std::endl;
  } else if (a == b) {
    std::cout << "a == b" << std::endl;
  }
  return 0;
}