using namespace std;

bool IsPrime(long num)
{
    if (num < 2) return false;
    else if (num == 2) return true;
    else if (num % 2 == 0) return false; // 偶数はあらかじめ除く

    double sqrtNum = sqrt(num);
    for (long i = 3; i <= sqrtNum; i += 2)
    {
        if (num % i == 0)
        {
            // 素数ではない
            return false;
        }
    }

    // 素数である
    return true;
}

int main() {
    
    long n;
    cin >> n;
    long ans = 0;
    for (long i=0;i<n;i++){
        long p;
        cin >> p;
        if(IsPrime(p)){
            ans += 1;
        }
    }
    cout << ans << endl;

}
