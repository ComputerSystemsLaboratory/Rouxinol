#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  unsigned short n;
  std::string Param;

  while (true) {

    getline(std::cin, Param);
    std::istringstream iss(Param);
    iss >> n;

    if (n > 1000) {
      std::cout << "Error: 1 <= x <= 1000" << std::endl;
    } else {
      break;
    }
  }

  std::string TaroCard, HanakoCard;
  unsigned short Taro, Hanako;
  Taro = Hanako = 0;

  for (size_t i = 0; i < n; i++) {
    getline(std::cin, Param);
    std::istringstream iss(Param);
    iss >> TaroCard >> HanakoCard;
    iss.clear();
    if (TaroCard > HanakoCard) {
      Taro += 3;
    } else if (TaroCard < HanakoCard) {
      Hanako += 3;
    } else {
      Taro++;
      Hanako++;
    }
  }
  std::cout << Taro << " " << Hanako << std::endl;
  return 0;
}