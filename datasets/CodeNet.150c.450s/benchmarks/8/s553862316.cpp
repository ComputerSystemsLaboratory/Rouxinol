#include<iostream>
#include<string>
using namespace std;

char Judge(string WordT, string WordH){
	for(int i = 0; i < min(WordT.size(), WordH.size()); i++){
		if(WordT[i] > WordH[i]) return 'T';
		if(WordT[i] < WordH[i]) return 'H';
	}
	if(WordT.size() >  WordH.size()) return 'T';
	if(WordT.size() <  WordH.size()) return 'H';
	return 'D';
}

int main(){
	//???????????????????????°n????????????
	int n;
	cin >> n;
	//???????????????????????????????????????????????????????????¨????????????????????????
	int PointT = 0;
	int PointH = 0;
	for(int i = 0; i < n; i++){
		string WordT;
		string WordH;
		cin >> WordT >> WordH;
		if(Judge(WordT, WordH) == 'T') PointT += 3;
		if(Judge(WordT, WordH) == 'D') PointT++, PointH++;
		if(Judge(WordT, WordH) == 'H') PointH += 3;
	}
	//????¨????????????¨?????????
	cout << PointT << " " << PointH << endl;
	return 0;
}