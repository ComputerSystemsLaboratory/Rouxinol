#include<iostream>
#include<string>
#include<vector>
using namespace std;

int judge(string WordT, string WordH){
	int j = 0;
	int Judge = 0;
	while(j < WordT.size() && j < WordH.size()){
		if(WordT[j] != WordH[j]){
			if(WordT[j] > WordH[j]) Judge = 1;
			if(WordT[j] < WordH[j]) Judge = -1;
			break;
		}
		j++;
	}
	if(Judge == 0 && WordT.size() > WordH.size()) Judge = 1;
	if(Judge == 0 && WordT.size() < WordH.size()) Judge = -1;
	return Judge;
}

