#include <bits/stdc++.h>
#define REP(i,n) for(int i=0;i<(n);i++)
#define ALL(v) (v).begin(),(v).end()
#define fir first
#define sec second
#define PB push_back
#define MP make_pair
#define ll long long
using namespace std;
int main(){
    int n,m;
    while(1){
        cin>>n>>m;
        if(n==0&&m==0)break;
        vector<pair<int,int> >V;
        int ans=0;
        REP(i,n){
            int a,b;
            cin>>a>>b;
            V.PB(MP((-1)*b,a));
            ans+=a*b;
        }
        sort(ALL(V));
        for(int i=0;i<V.size()&&m>0;i++){
            int a=V[i].fir*(-1);
            int b=V[i].sec;
            if(b<=m){
                m-=b;
                ans-=a*b;
            }else{
                ans-=a*m;
                break;
            }
        }
        cout<<ans<<endl;
    }
}