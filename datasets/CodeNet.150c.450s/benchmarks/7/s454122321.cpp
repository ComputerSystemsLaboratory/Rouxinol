#include <stdio.h>

#define sort 10

int main()
{
	int bab[sort], temp;
	for (int n = 0; n < sort; n++)
	{
		scanf("%d", &bab[n]);
	}
	for (int i = 0; i < sort - 1; i++)
	{
		for (int j = sort -1; j > i; j--)
		{
			if (bab[j - 1] < bab[j])
			{
				temp = bab[j];
				bab[j] = bab[j - 1];
				bab[j - 1] = temp;
			}
		}
	}
	for (int m = 0; m < 3; m++)
	{
		printf("%d\n", bab[m]);
	}
	return 0;
}