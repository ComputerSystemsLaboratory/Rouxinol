#include <iostream>
using namespace std;

int main(){
  int cost, number;
  int coin[21];
  int T[21][50001];
  cin >> cost >> number;
  for(int i = 1; i <= number; i++){
    cin >> coin[i];
  }
  for(int i = 0; i <= number; i++){
    T[i][0] = 0;
  }
  for(int j = 0; j <= cost; j++){
    T[0][j] = 50000;
  }
  for(int i = 1; i <= number; i++){
    for(int j = 1; j <= cost; j++){
      if(j < coin[i]){
	T[i][j] = T[i-1][j];
      }
      else{
	T[i][j] = min(T[i-1][j], T[i][j-coin[i]]+1);
      }
    }
  }
  cout << T[number][cost] << endl;
}

