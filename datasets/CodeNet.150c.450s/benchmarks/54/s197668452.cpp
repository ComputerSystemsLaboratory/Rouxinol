#include<iostream>
#include<string>
#include<vector>
using namespace std;

string Change(string Letter){
	for(int i = 0; i < Letter.size(); i++){
		if('a' <= Letter[i] && Letter[i] <= 'z') Letter[i] += 'A' - 'a';
	}
	return Letter;	
}	

string Transition(string Word){
	Word = Change(Word);
	return Word;
}

vector<string> ReadWords(){
//	?????????????????????????????????END_OF_TEXT??§???????????°??§???????????´?????????????????????????????????
	vector<string> Sentence;
	while(1){
		string Go;
		cin >> Go;
		if(Go == "END_OF_TEXT") break;
		Go = Change(Go);
		Sentence.push_back(Go);
	}
	return Sentence;
}

int Count(string Word, vector<string> Sentence){
//	?????????????????????????????????????????????????????°?????????
	int C = 0;
	for(int i = 0; i < Sentence.size(); i++){
		if(Sentence[i] == Word) C++;
	}
	return C;
}

