#include <iostream>
#include <sstream>

int main(int argc, char const *argv[]) {
  std::string x;
  unsigned short NumberOfInt = 0;
  while (true) {
    while (true) {
      getline(std::cin, x);
      const auto result = x.find_first_not_of("0123456789");
      NumberOfInt = x.length();
      if (result != std::string::npos || NumberOfInt > 1200) {
        std::cout << "Error: Input string can only contain integer and must be "
                     "less than 1200 characters."
                  << std::endl;
      } else {
        break;
      }
    }
    if (x == "0") {
      break;
    } else {
      unsigned short Sum = 0;

      for (size_t i = 0; i < NumberOfInt; i++) {
        Sum += x[i] - '0';
      }

      std::cout << Sum << std::endl;
    }
  }
  return 0;
}