int dat[52][52]; // ??¨?????????1???????????£?????°

void delete_island_from(int i, int j)
{
	int dx[8] = {-1,-1,-1, 0,  0, 1, 1, 1};
	int dy[8] = {-1, 0, 1,-1,  1,-1, 0, 1};

	dat[i][j] = 0;
	for(int k=0;k<8;k++) {
		if(dat[i+dx[k]][j+dy[k]]) {
			delete_island_from(i+dx[k],j+dy[k]);
		}
	}
}


