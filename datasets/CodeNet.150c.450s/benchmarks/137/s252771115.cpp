#include <iostream>
#include <unordered_set>
#include <string>
using namespace std;

int main(){
	unordered_set<string> S;
	string cmd;
	string word;
	int n;
	cin>>n;
	for(int i=0;i<n;++i){
		cin>>cmd;
		if(cmd=="insert"){
			cin>>word;
			S.insert(word);
		}else if(cmd=="find"){
			cin>>word;
			if(S.count(word)==0)
				cout<<"no"<<endl;
			else
				cout<<"yes"<<endl;
		}
	}
}