#include <iostream>
#include <sstream>
#include <vector>

int main(int argc, char const *argv[]) {
  std::vector<std::vector<std::vector<int>>> Residents(
      4, std::vector<std::vector<int>>(3, std::vector<int>(10, 0)));
  unsigned int n = 1;
  std::string Param;

  getline(std::cin, Param);
  std::istringstream iss(Param);
  iss >> n;
  iss.clear();

  unsigned short b, f, r;
  short v;
  b = f = r = v = 0;

  unsigned short count = n;
  while (count) {
    getline(std::cin, Param);
    iss.str(Param);
    iss >> b >> f >> r >> v;
    iss.clear();
    b--;
    f--;
    r--;
    Residents[b][f][r] += v;
    count--;
  }
  unsigned short NumOfBuildings = Residents.size();
  for (const auto &Buildings : Residents) {
    for (const auto &Floors : Buildings) {
      for (const auto &Rooms : Floors) {
        std::cout << " " << Rooms;
      }
      std::cout << std::endl;
    }
    if (NumOfBuildings > 1) { // Don't print separator below the last building
      std::cout << "####################" << std::endl;
    }
    NumOfBuildings--;
  }
  return 0;
}