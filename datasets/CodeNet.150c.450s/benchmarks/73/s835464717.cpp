//??????[a,b)???????????¨??????, ???????????? k ?????????????????????l,r????????????.
int find(int a,int b,int k,int l,int r){
	//if(a==b) return data[a+n-1];
	if(r<=a || b<=l) return 0;
	if(a<=l && r<=b) return data[k];
	else{
		int vl=find(a,b,k*2+1,l,(l+r)/2);
		int vr=find(a,b,k*2+2,(l+r)/2,r);
		return vl+vr;
	}
}

