int countWay(int limit, int target)
{
	int counter = 0;

	for(int i = 1; i <= limit-2; i++)
	{
		for(int j = i+1; j <=limit-1; j++)
		{
			for(int k = j+1; k <= limit; k++)
			{
				if(i+j+k == target)
				{
					counter++;
				}
			}
		}
	}
	return counter;
}

