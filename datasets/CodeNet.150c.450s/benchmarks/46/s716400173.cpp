#include <iostream>
#include <sstream>
#include <vector>

int main(int argc, char const *argv[]) {
  unsigned short n, x;
  n = 5;
  x = 9;
  while (true) {
    while (true) {
      std::string Param;

      getline(std::cin, Param);
      std::istringstream iss(Param);
      iss >> n >> x;

      if (n == 1 || n == 2 || n > 100) {
        std::cout << "Error: 3 <= n <= 100" << std::endl;
      } else {
        break;
      }
    }
    if (n == 0 && x == 0) {
      break;
    } else {
      unsigned short MatchCount = 0;

      for (size_t i = 1; i < n + 1; i++) {
        for (size_t j = 1; j < n + 1; j++) {
          for (size_t k = 1; k < n + 1; k++) {
            if (i > j && j > k) {
              if ((i + j + k) == x) {
                MatchCount++;
              }
            }
          }
        }
      }
      std::cout << MatchCount << std::endl;
    }
  }
  return 0;
}