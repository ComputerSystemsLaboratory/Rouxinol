ll power(ll m,ll n, ll mod = MOD ){
	ll ans = 1;
	ll mbin = m;
	for (ll i = 1 ; i <= n; i *= 2) {
		if ((n & i) != 0) {
			ans *= mbin; ans %= mod;
		}
		mbin = (mbin * mbin) % mod;
	}
	return ans;
}

