vector<int> bellman(Graph &G, int s) {
    // ???????????°??¨????????°?????°??????
    int V = G.size();
    vector<int> d(V);
    for(int i=0; i<V; i++) d[i] = INF;
    d[s] = 0;

    while(true) {
        bool update = false;
        for(int i=0; i<V; i++) {
            for(int j=0; j<G[i].size(); j++) {
                Edge e = G[i][j];
                if(d[i] != INF && d[e.to] > d[i] + e.cost) {
                    d[e.to] = d[i] + e.cost;
                    update = true;
                }
            }
        }
        if(!update) break;
    }
    return d;
}

