#include <iostream>
using namespace std;


int main() {

    while(true)
    {
        //?????????????????°??\???
        int cards[2];
        cin >> cards[0] >> cards[1];
        //0??????????????????
        if(cards[0] == 0)
            break;
        //????????????????????¨??????????¨???\???
        int taro[cards[0]], hanako[cards[1]];
        int t_sum=0, h_sum=0, gap=0;
        
        for(int i = 0; i<cards[0];i++)
        {
            cin >> taro[i];
            t_sum+=taro[i];
        }
        for(int i=0;i<cards[1];i++)
        {
            cin >> hanako[i];
            h_sum+=hanako[i];
        }
        //????¨???????
        gap = h_sum - t_sum;
        if(gap%2 != 0) //???????????°??§????????¨???????????????
        {
            cout << -1 << endl;
        }
        else
        {
            gap /= 2;//???????????????????????????????????????
        
            int output[2] = {-1,-1};//??????????????°???
            int out_sum = 300;//??°?????????
            for(int i = 0;i<cards[0];i++)
                for(int j = 0;j<cards[1];j++)
                {
                    if(hanako[j] - taro[i] == gap && hanako[j] + taro[i] < out_sum)
                    {
                        output[0] = taro[i];
                        output[1] = hanako[j];
                        out_sum = output[0] + output[1];
                    }
                
                }
            if(output[0] == -1)
                cout << -1 << endl;
            else
                cout << output[0] << " " << output[1] << endl;
        }
        
    }
    return 0;
}