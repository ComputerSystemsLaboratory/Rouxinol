#include <iostream>
#include <climits>
using namespace std;
const int SHEET_LIMIT = 100;

bool calc(const int n[], const int m[], int npos, int mpos, int nite, int mite) {
	int TaroSum = 0;
	int HanakoSum = 0;
	for(int i = 0; i < nite; i++){
		if(i != npos){
			TaroSum += n[i];
		}else{
			TaroSum += m[mpos];
		}
	}
	for(int i = 0; i < mite; i++){
		if(i != mpos){
			HanakoSum += m[i];
		}else{
			HanakoSum += n[npos];
		}
	}
	if(TaroSum == HanakoSum){
		return true;
	}
	return false;
}
