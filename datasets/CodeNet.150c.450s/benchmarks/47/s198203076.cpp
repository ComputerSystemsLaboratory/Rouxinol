#include <iostream>

int main()
{

    int W,H,x,y,r ;
    
    bool xYes = false;
    bool yYes = false;
    
    std::cin >> W >> H >> x >> y >> r ;
    
    if ((x - r) < 0 || (x + r) > W){
        
        xYes = false;
        
    }else if ((x - r) >= 0 || (x + r ) <= W){
        xYes = true;
        
    }
    
    if (y - r < 0 || y + r > H ){
        
        yYes = false;
    }else if(y - r >= 0 || y + r <= H){
        yYes = true;
    }
    
    if (xYes && yYes){
        std::cout << "Yes" << "\n";
    }else{
        std::cout << "No" << "\n";
    }
    
    
    return 0;
}