void insert(int x) {
    H++;
    maxHeap[H] = x;
    
    int i = H;
    
    while(i>1 && maxHeap[parentOf(i)] < maxHeap[i]) {
        swap(maxHeap[parentOf(i)], maxHeap[i]);
        i = parentOf(i);
    }
}


