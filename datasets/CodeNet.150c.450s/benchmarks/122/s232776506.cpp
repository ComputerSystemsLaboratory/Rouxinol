int maxHeap[2000001];
int H;

int parentOf(int i) {
    return i/2;
}

int leftChildOf(int i) {
    return i*2;
}

int rightChildOf(int i) {
    return i*2+1;
}

void maxHeapify(int i) {
    //    dout << "---------\n";
    //    dout << "maxHeapify( " << i << " ) : " << "key = " << maxHeap[i] << "\n";
    
    int largest;
    int left = leftChildOf(i);
    int right = rightChildOf(i);
    
    //i,?????????,????????????3????????????????????§??????????????¢???
    if( left<=H && maxHeap[left]>maxHeap[i] ) largest = left;
    else largest = i;
    
    if( right<=H && maxHeap[right]>maxHeap[largest] ) largest = right;
    
    if( largest != i ) { //?????§????????????i??§???????????°???i??¨largest?????????swap?????????largest??\??????????§????
        int tmp = maxHeap[i];
        maxHeap[i] = maxHeap[largest];
        maxHeap[largest] = tmp;
        
        maxHeapify(largest); //recursive call
    }
}

void buildMaxHeap() {
    for(int i=H/2; i>0; i--) {
        maxHeapify(i);
    }
}


