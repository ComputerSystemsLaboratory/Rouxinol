int markToNum(char mark){
	if(mark == 'S'){
		return 0;
	}else if(mark == 'H'){
		return 1;
	}else if(mark == 'C'){
		return 2;
	}else{
		return 3;
	}
}

char numToMark(int num){
	if(num == 0){
		return 'S';
	}else if(num == 1){
		return 'H';
	}else if(num == 2){
		return 'C';
	}else{
		return 'D';
	}
}
