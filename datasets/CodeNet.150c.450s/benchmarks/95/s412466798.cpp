#include <iostream>
#include <string>

int main(void) {
    int n;
    std::string s;
    int count;
    std::string now_status;
    while (1) {
        std::cin >> n;
        if (n == 0) {
            break;
        }
        count = 0;
        now_status = "";
        for (int i = 0; i < n; i++) {
            // ??????????????????????¶???????????????????????????????????????????
            // ????????°???????????????????¶??????????????????????????????????????????\?????????????????¨?????????????????????
            std::cin >> s;
            if (now_status == "") {
                now_status = s;
                continue;
            }
            if (now_status[0] != s[0] && now_status[1] == s[1]) {
                count++;
            } 
            now_status = s;
        }
        std::cout << count << std::endl;
    }
    return 0;
}