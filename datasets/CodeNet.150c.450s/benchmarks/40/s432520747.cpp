#include <iostream>
#include <string>
using namespace std;

class DICE{
private:
	int D[6];//??????????????????
	
public:
	DICE();
	void set();
	void show();
	void roll(char order);
};


//?????????????????????	??????????????????????????????
DICE::DICE(){
	for(int i = 0; i < 6; ++i){
		D[i] = i+1;
	}
}



//??????????????????????????´=========================================
void DICE::set(){
	
	int number[6];
	
	for(int i = 0; i < 6; ++i){
		cin >> number[i];
	}
	
	D[0] = number[0];//1
	D[1] = number[1];//2
	D[2] = number[2];//3
	D[3] = number[3];//4
	D[4] = number[4];//5
	D[5] = number[5];//6
}



//??????????????????????????¨???========================================-
void DICE::show(){
	cout << D[0] << endl;
}



//?????????????????¢??????==========================================-
void DICE::roll(char order){
	
	int exchange;
		
	if(order == 'N'){//???????????????1????????¢??????
		exchange = D[0];
		D[0] = D[1];
		D[1] = D[5];
		D[5] = D[4];
		D[4] = exchange;
	}
		
	if(order == 'S'){//???????????????1????????¢??????
		exchange = D[0];
		D[0] = D[4];
		D[4] = D[5];
		D[5] = D[1];
		D[1] = exchange;
	}

	if(order == 'E'){//??±????????????1????????¢??????
		exchange = D[0];
		D[0] = D[3];
		D[3] = D[5];
		D[5] = D[2];
		D[2] = exchange;
	}

	if(order == 'W'){//?\?????????????1????????¢??????
		exchange = D[0];
		D[0] = D[2];
		D[2] = D[5];
		D[5] = D[3];
		D[3] = exchange;
	}
}





int main(){
	
	string order;
	DICE dice;
	
	
	dice.set();
	
	cin >>order;
	for(int i = 0; i < order.size(); ++i){
		dice.roll(order[i]);
	}
	
	dice.show();
	
	
	return 0;
}