#include <stdio.h>
#include <iostream>
#include <algorithm>

using namespace std;

int main(int argc, char* argv[])
{
	int i;
	int n;
	int a[100];

	cin >> n;

	for (i = 0; i < n; i++) {
		cin >> a[i];
	}
	for (i = n-1; i >= 0; i--) {
		cout << a[i];
		if (i != 0) cout << " ";
	}
	cout << endl;
}