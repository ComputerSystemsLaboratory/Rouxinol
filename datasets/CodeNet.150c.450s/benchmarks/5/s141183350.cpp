#include <iostream>
#include <sstream>
#include <vector>

int main(int argc, char const *argv[]) {
  unsigned int n = 1;
  std::string Param;

  while (true) {

    getline(std::cin, Param);
    std::istringstream iss(Param);
    iss >> n;

    if (n < 1 || n > 100) {
      std::cout << "Error: 1 <= n <= 100" << std::endl;
    } else {
      break;
    }
  }

  // unsigned int Ai[n];
  std::vector<unsigned int> Ai(n, 0);
  while (true) {
    bool isValid = true;

    getline(std::cin, Param);
    std::istringstream iss(Param);
    for (size_t i = 0; i < n; i++) {
      iss >> Ai[i];
      if (Ai[i] > 1000) {
        std::cout << "Error: 0 <= Ai <= 1000" << std::endl;
        isValid = false;
        break;
      } else {
        isValid = true;
      }
    }
    if (isValid == true) {
      break;
    }
  }
  for (size_t i = n - 1; i > 0; i--) {
    std::cout << Ai[i] << " ";
  }
  std::cout << Ai[0] << std::endl;
  
  return 0;
}