#include<iostream>
#include<algorithm>
using namespace std;

#define DAY_TIME 86400

//??¢??°(00:00:00??????????§???°?????????)
int timeToNum(char* text) {
	int ans=0;
	//h
	ans += (text[0] - '0') * 60 * 60 * 10;
	ans += (text[1] - '0') * 60 * 60;
	//m
	ans += (text[3] - '0') * 60 * 10;
	ans += (text[4] - '0') * 60;
	//s
	ans += (text[6] - '0') * 10;
	ans += (text[7] - '0');

	return ans;
}

//?????????????????????
int imosu[DAY_TIME];

//??????????????¨??????
int N;

//??????????????¨??°?????????
char timeS[100];
char timeE[100];

//??????
int ans;

int main() {
	while (true) {
		//?????????
		for(int i=0;i<DAY_TIME;i++)imosu[i]=0;
		ans=0;

		//??\?????¨???????????¶
		cin>>N;
		if(N==0)break;

		for(int i=0;i<N;i++){
			//??\???
			cin >> timeS;
			cin >> timeE;
			
			//???????????????????????????
			imosu[timeToNum(timeS)]++;
			imosu[timeToNum(timeE)]--;
		}

		//????????????????????????????????????
		for (int i = 0; i < DAY_TIME-1; i++) {
			imosu[i+1]+=imosu[i];
			ans=max(ans,imosu[i+1]);
		}
		//?????§????????????
		cout<<ans<<endl;
	}
	return 0;
}