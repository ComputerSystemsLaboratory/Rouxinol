#include <iostream>
#include <math.h>
#include <vector>
using namespace std;

//vector??????????§£???
int main(){
	while (1){
		int n;
		cin >> n;
		//???????????¶
		if (n == 0) break;
		//??\???
		vector<int> num;
		for (int i = 0; i < n; i++){
			int tmp;
			cin >> tmp;
			num.push_back(tmp);
		}
		//????¨????????°????????????§???????¨????
		int max = -1;
		int min = 99999;
		int sum = 0;
		for (int i = 0; i < num.size(); i++){
			if (num[i] >= max) max = num[i];
			if (num[i] <= min) min = num[i];
			sum += num[i];
		}
		//??????????¨????
		int avg = floor((sum - max - min) / (n - 2));
		cout << avg << endl;
	}
	return 0;
}