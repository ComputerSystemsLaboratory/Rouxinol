int main(){
  cin.tie(0);
  ios_base::sync_with_stdio(false);

  VI sum(1001, 0);
  for(int i=1;i<=1000;++i) sum[i] = sum[i-1] + i;
  while(true){
	int N; cin >> N; if(!N) break;
	int cnt = 0;
	for(int a=1;a<=1000;++a)
	  for(int b=a+1;b<=1000;++b)
		if(sum[b] - sum[a-1] == N)
		  ++cnt;
	cout << cnt << endl;
  }
  return 0;
}