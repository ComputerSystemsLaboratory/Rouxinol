void swap(int& x, int& y) {
	int temp = x;
	x = y;
	y = temp;
}

int main(void) {
	int N, a[100], i, j, k=0;
	/*printf("??´??°?????\????????????????????????");*/
	scanf("%d", &N);
	/*printf("%d??????????´??????\????????????????????????",N);*/
	for (i = 0; i < N; i++)
		scanf("%d", &a[i]);
	for (i = 0; i < N; i++) {
		for (j = N - 1; j > i; j--) {
			if (a[j] < a[j - 1]) {
				swap(a[j], a[j - 1]);
				k++;
			}
		}
	}
	for (i = 0; i < N; i++)
		printf("%d%s", a[i], i < N-1 ? " " : "\n");
	printf("%d\n", k);
	return 0;
}