int main(){

  int n, x, y;

  while(std::cin >> n){

    if(n == 0) break;

    for(int i=0; i<21; i++){
      for(int j=0; j<21; j++){
        //MAP????????????
        MAP[i][j] = false;
      }
    }


    for(int i=0; i<n; i++){
      std::cin >> x >> y;
      MAP[x][y] = true;
    }

    int m;
    int robot_x = 10, robot_y = 10; //???????????????????????????
    int num_of_get = 0; //??\?????§?????????????????°

    std::cin >> m;

    for(int i=0; i<m; i++){

      char direction;
      int distance;

      std::cin >> direction >> distance;

      for(int j=0; j<distance; j++){
        //??????:1?????????????§??????????????????¨?????????????????????????????????
        //     ????????£??????num_of_get??????????????????????????????????????????(true -> false)
        switch(direction){
          case 'E':
            robot_x++;
            break;

          case 'W':
            robot_x--;
            break;

          case 'N':
            robot_y++;
            break;

          case 'S':
            robot_y--;
            break;
        }

        if(MAP[robot_x][robot_y] == true){
          num_of_get++;
          MAP[robot_x][robot_y] = false;
        }

      }

    }

    if(num_of_get == n){
      std::cout << "Yes" << std::endl;
    }else{
      std::cout << "No" << std::endl;
    }

  }

  return 0;

}