#include <iostream>
#include <stdio.h>
#include <string>
using namespace std;

class DICE{
private:
	int D[6];//??????????????????
	
public:
	DICE();
	void set(int*);
	int show();
	void roll(char);
	void change(int);
};


//???????????????????????????????????????????????????
DICE::DICE(){
	
	for(int i = 0; i < 6; ++i){
		D[i] = i+1;
	}
}



//??????????????????????????´=========================================
void DICE::set(int *number){
	
	for(int i = 0; i < 6; ++i){
		D[i] = number[i];
	}
}



//??????????????????????????¨???=========================================
int DICE::show(){
	
	return D[0];
}



//?????????????????¢??????===========================================
void DICE::roll(char order){
	
	int exchange;
		
	if(order == 'N'){//???????????????1????????¢??????
		exchange = D[0];
		D[0] = D[1];
		D[1] = D[5];
		D[5] = D[4];
		D[4] = exchange;
	}
		
	if(order == 'S'){//???????????????1????????¢??????
		exchange = D[0];
		D[0] = D[4];
		D[4] = D[5];
		D[5] = D[1];
		D[1] = exchange;
	}

	if(order == 'E'){//??±????????????1????????¢??????
		exchange = D[0];
		D[0] = D[3];
		D[3] = D[5];
		D[5] = D[2];
		D[2] = exchange;
	}

	if(order == 'W'){//?\?????????????1????????¢??????
		exchange = D[0];
		D[0] = D[2];
		D[2] = D[5];
		D[5] = D[3];
		D[3] = exchange;
	}
}



//??????????????´==================================================
void DICE::change(int n){
	
	for(int i = 0; i < 5; ++i){
		
		if(D[0] == n)	{ break; }
		if(i % 2 == 0)	{ roll('N'); }
		if(i % 2 == 1)	{ roll('W'); }
	}
}





int main(){
	
	int number[6];
	int n, x, y;
	DICE dice;
	
	//???????????????????????\???======================
	for(int i = 0; i < 6; ++i){
		cin >> number[i];
	}
	dice.set(number);
	
	
	//??????===================================
	cin >> n;
	
	for(int i = 0; i < n; ++i){
		
		cin >> x >> y;
		
		
		dice.change(x);
		dice.roll('N');
		
		
		while(1){
			if(dice.show() == y) break;
			dice.roll('W');
		}
		
		
		dice.roll('W');
		cout << dice.show() << endl;
	}
	
	return 0;
}