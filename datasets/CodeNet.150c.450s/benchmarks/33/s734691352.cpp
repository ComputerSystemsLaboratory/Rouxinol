int main() {
	int X;
	int Y;
	int S;
	vector<int> A_array;
	vector<int> B_array;

	for (int a = 0; a < 1000; a++) {
		for (int b = 0; b < 1000; b++) {
			if ((a > 0) && (b > 0)) {
				A_array.push_back(a);
				B_array.push_back(b);
			}
			
		}
	}


	while(true) {

	cin >> X >> Y >> S;

	if ((X == 0) && (Y == 0) && (S == 0))
		break;
		
	vector<int> newvalue;
	int maxvalue = 0;

	for(int j = 0; j < A_array.size(); j++) {
		int taxsum = A_array[j]*(X+100)/100 + B_array[j]*(X+100)/100;
		int newtaxsum = A_array[j]*(Y+100)/100 + B_array[j]*(Y+100)/100;
		if ((S == taxsum) && (A_array[j] < S) && (B_array[j] < S) && (S > 10) && (S < 1000))
			newvalue.push_back(newtaxsum);
	}


	for(int k=0; k<newvalue.size(); k++) {
		if (maxvalue < newvalue[k]) {
			maxvalue = newvalue[k];
		}
	}

	cout << maxvalue << endl;

	}

	return 0;
}