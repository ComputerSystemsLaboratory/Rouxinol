int N, W;
int w[MAX_N], v[MAX_N];

// dp table
int dp[MAX_N + 1][MAX_W + 1];


// i: i????????\?????? item ?????????
// rw: ?????????????????? w ??\???
// return: ???????????????????????§???
int rec(int i, int rw) {
  // dp lookup
  if (dp[i][rw] > 0) {
    return dp[i][rw];
  }

  int res;
  if(i == N) {
    // ?????????????????????
    res = 0;
  } else if ( rw < w[i] ) {
    // ??\?????????
    res = rec(i + 1, rw);
  } else {
    // ??\???????????¨????????????
    // ??\????????´?????¨??\???????????´??????????????????
    res = max( rec(i + 1, rw), rec(i + 1, rw - w[i]) + v[i] );
  }

  dp[i][rw] = res;
  return res;

}



int main() {

  memset(dp, -1, sizeof(dp));

  cin >> N >> W;
  for (int i = 0; i < N; i++) {
    cin >> v[i] >> w[i];
  }

  cout << rec(0, W) << endl;

}