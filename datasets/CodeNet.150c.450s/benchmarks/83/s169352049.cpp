#include <bits/stdc++.h>
using namespace std;

#define INF 1e+9

int N,W;
int w[101],v[101];
int memo[110][11000];

int knapsack(int i, int j){
	  if(j < 0) return -INF; // ??????????¶?????????????
	  if(i == N) return 0;   // ????????????????????????
	  if(memo[i][j] != -1) return memo[i][j]; //???????¨??????????

	  int ans= 0;
	  ans = max(knapsack(i+1,j), knapsack(i+1, j-w[i]) + v[i]);

	  memo[i][j] = ans;
	  return ans;
}

int main(){
	  cin >> N >> W;
	  for(int i=0; i<N; i++) cin >> v[i] >> w[i];
	  memset(memo,-1,sizeof(memo));
	  cout << knapsack(0,W) << endl;
}