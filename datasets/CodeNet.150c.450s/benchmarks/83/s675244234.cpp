#include <iostream>
#include <vector>

using namespace std;

#define MAX_N 100
#define MAX_W 10000
#define INF (1e9+1)

int main() {
	int N,W;
	cin>>N>>W;
	
	vector<int> v(N),w(N);
	for(int i=0;i<N;i++){
		cin>>v[i]>>w[i];
	}
	
	int dp[MAX_N+1][MAX_W+1];
	for(int i=0;i<MAX_N+1;i++){
		for(int j=0;j<MAX_W+1;j++){
			dp[i][j] = -INF;
		}
	}
	dp[0][0] = 0;
	
	for(int i=0;i<N;i++){
		for(int j=0;j<=W;j++){
			//i???????????????????????¨?????????????§?
			dp[i+1][j] = max( dp[i+1][j], dp[i][j] );
			
			if(j+w[i]<=W){	//??????????????§??????
				//i???????????????????????¨??????????§?
				dp[i+1][j+w[i]] = max( dp[i+1][j+w[i]], dp[i][j]+v[i] );
			}
		}
	}

	int ans = -INF;
	for(int j=0;j<=W;j++){
		ans = max( ans, dp[N][j] );
	}
	cout<<ans<<endl;
	return 0;
}