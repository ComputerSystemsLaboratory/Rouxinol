#include <iostream>

using namespace std;

int value[100],weight[100];
//table[i][w] ?????????i????????§???????????´??????w??\?????????????????????????????§???
int table[101][10001];
int main(){
  int N,W;
  cin>>N>>W;
  for(int i=0;i<N;++i){
    cin>>value[i]>>weight[i];
  }

  for(int i=1;i<=N;++i){
    for(int w=1;w<=W;++w){
      if(w<weight[i-1]){
        table[i][w] = table[i-1][w];
      }else{
        table[i][w] = max(table[i-1][w],table[i-1][w-weight[i-1]]+value[i-1]);
      }
    }
  }
  cout<<table[N][W]<<endl;
  return 0;
}