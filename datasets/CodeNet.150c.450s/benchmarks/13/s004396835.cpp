#include<stdio.h>

int main()
{
	int i,t,n,m=0,g,h,k,q,c[50]={},d[50]={};
	
	char s[200],p[200];
	
	scanf("%s %s",s,p);
	
	for(i=0;s[i]!='\0';i++)n=i+1;
	//printf("%d\n",n);
	
	for(t=0;t<i;t++){
		
		s[t+i]=s[t];
	}
	s[t+i]='\0';
	//printf("%s\n",s);//???????????§??§n???s[]???????????°
	
	for(i=0;p[i]!='\0';i++);
	
	for(t=0;t<n;t++){
		
		//printf("%c %c %d\n",s[t],p[0],m);
		if(s[t]==p[0]){
			c[m]=t;
			m++;
		}//???????????§??§c[m]??????????\´??????????????§?????????????????????????????????????????°
	}
	//printf("\n");
	
	for(g=0;g<m;g++){
		h=0;
		q=c[g];
		d[g]=1;
		for(k=0;k<i;k++){
			
			if(s[q+k]!=p[k]){
				h=h+1;
			}
			//printf("%c %c %d\n",s[q+k],p[k],h);
			if(d[g]!=0||h!=0){
				
				d[g]=h;
			}
		}
		//printf("\n");
		
	}
	i=0;
	for(g=0;g<m;g++){
		
		//printf("%d\n",d[g]);
		if(d[g]==0){
			
			printf("Yes\n");
			i=1;
			break;
		}
	}
	if(i!=1){
		printf("No\n");
	}
	
	return 0;
}