#include <iostream>
#include <algorithm>
#define LEN 100005
using namespace std;

//?????????????????¨????§???????
typedef struct pp{
	char name[100];
	int t;
}P;

P Q[LEN];
int head, tail, n;

void enqueue(P x){
	Q[tail] = x;
	tail = (tail + 1) % LEN;
}

P dequeue(){
	P ret = Q[head];
	head = (head + 1) % LEN;
	return ret;
}

int main()
{
	//??????????????°
	int n;

	//???????????????
	int q;

	cin >> n;
	cin >> q;

	for (int i = 1; i <= n; i++){
		cin >> Q[i].name;
		cin >> Q[i].t;
	}

	head = 1;
	tail = n + 1;

	//????????????
	int elaps = 0;

	//sim
	while (tail != head){
		P u = dequeue();

		//??°????°?????????????????????¨
		int c = min(u.t, q);

		//?????????????¨????
		u.t -= c;

		//?????????????????????
		elaps += c;

		if (u.t > 0){
			enqueue(u);
		}
		else {
			cout << u.name << ' ' << elaps << '\n';
		}
	}

	return 0;
}