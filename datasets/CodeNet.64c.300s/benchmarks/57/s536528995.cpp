#include <cstdio>
#include <cmath>
#include <algorithm>

#define R_MAX 10
#define C_MAX 10000

// ???????£?????????¶?????? 2^r ????????¨??¢?´¢?????????
// ??????????£????????????????????????§???????????§???
// ???????£?????????¶???????±?????????°???????£?????????¶??????????????§??¬???????±????????????????
