int solve(int senbeis[R_MAX][C_MAX], int r, int c) {
  int result = 0;

  // ???????£?????????¶????????¨??¢?´¢??????
  // i ??? n bit ?????? 1 ?????¨??? n-th row ????????£?????????????????¨?????????
  int bitMax = pow(2, r);
  for (int i = 0; i < bitMax; i++) {
    int baked = 0; // ?£??????????????????°
    for (int j = 0; j < c; j++) {
      int front_senbei = 0; // ??¨?????????????????°
      for (int k = 0; k < r; k++) {
        // i ??? k-th bit ?????????????????????1 ?????? senbeis[k][j] ??? 0,1 ????????¢
        front_senbei += (senbeis[k][j] ^ (i >> k & 1));
      }
      baked += std::max(front_senbei, r - front_senbei);
    }
    result = std::max(result, baked);
  }
  return result;
}

int main() {
  while (true) {
    int senbeis[R_MAX][C_MAX];
    int r, c;

    // input
    scanf(" %d %d", &r, &c);
    if (r == 0 || c == 0) break;
    for (int i = 0; i < r; i++) {
      for (int j = 0; j < c; j++) {
        scanf("%d", &senbeis[i][j]);
      }
    }

    // output
    printf("%d\n", solve(senbeis, r, c));
  }

  return 0;
}