#include <iostream>
#include <algorithm>

using namespace std;

int cntSort;

void printArray(int A[], int N) {
	for (int i = 0; i < N - 1; i++)
		cout << A[i] << " ";
	cout << A[N - 1] << endl;
}

void inputArray(int A[], int N) {
	for (int i = 0; i < N; i++) {
		cin >> A[i];
	}
}

void selectionSort(int A[], int N) {
	//i?????????????????¨??????????????????????????????
	for (int i = 0; i < N; i++) {
		int minj = i;	//minj???i??\?????§????°????????´??????????????????????
		for (int j = i + 1; j < N; j++) {
			if (A[j] < A[minj])
				minj = j;
		}
		if (minj != i) {
			swap(A[minj], A[i]);
			cntSort++;
		}
	}
}

int main() {
	int N;
	cin >> N;
	int A[N];
	inputArray(A, N);
	selectionSort(A, N);
	printArray(A, N);
	cout << cntSort << endl;
}