using namespace std;

long long int saiki ( int N , int M , int O )
{
	if( N == 1 )
	{
		return M > O&&M<=9;
	}

	long long int ans = 0;
	for( int i = O + 1; i <= min ( 9 , M ); i++ )
	{
		ans += saiki ( N - 1 , M - i , i );
	}
	return ans;
}

int main ()
{
	int N , M;
	while( cin >> N >> M , N || M )
	{
		cout << saiki ( N , M , -1 ) << endl;
	}
}