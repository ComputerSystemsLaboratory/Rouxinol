string henkan(string str)//?°????????????§??????????????§??????????°????????????????
{
	int len = str.length();
	
	for(int i = 0; i < len; i++){
		if('A' <= str[i] && str[i] <= 'Z'){
			str[i] += 32;
		}
		else if('a' <= str[i] && str[i] <= 'z'){
			str[i] -= 32;
		}
	}
	return str;
}

