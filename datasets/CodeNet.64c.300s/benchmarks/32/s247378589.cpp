#include <iostream>
#include <cstdio>

using namespace std;

int main() {
	int a[10000];
	int n;
	int max=-1000000,min=1000000;
	long long sum=0;
	
	cin >> n;
	if(n==0){
		printf("%d %d %d\n", 0, 0, 0);
		return 0;
	}
	
	for(int i=0; i<n; i++){
		cin >> a[i];

		sum+=a[i];
		if(max<a[i]) max = a[i];
		if(min>a[i]) min = a[i];
	}
	
	printf("%d %d %lld\n", min, max, sum);
		

	return 0;
}