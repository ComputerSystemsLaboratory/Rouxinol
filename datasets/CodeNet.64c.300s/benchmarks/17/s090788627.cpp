int tax(int p, int price){
	return price * (100 + p) / 100;
}

int main(){
	int x, y, s;
	cin >> x;
	while(x != 0){
		cin >> y >> s;
		int maximum = 0;
		for(int i = 1; i < s; i++){
			for(int j = i; j < s; j++){
				if(tax(x, i) + tax(x, j) == s){
					maximum = max(maximum, tax(y, i) + tax(y, j));		
				}
			}
		}

		cout << maximum << endl;

		cin >> x;
	}

	return 0;
}