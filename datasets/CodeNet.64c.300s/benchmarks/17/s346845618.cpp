#include<iostream>
using namespace std;
int X, Y, S;
int solve() {
  int p = 1, q = S-1, r, u, t, max = 0;
  while(p <= S/2) {
    r = (100 * p + 99) / (100 + X);
    u = (100 * q + 99) / (100 + X);
    if ( r*(100+X)/100 != p || u*(100+X)/100 != q) goto next;
    t = r * (100 + Y) / 100 + u * (100 + Y) / 100;
    if (t > max) max = t;
  next:
    ++p;
    --q;
  }
  return max;
}
  
int main(){
  while(cin >> X >> Y >> S && X > 0) {
    cout << solve() <<endl;
  }
  return 0;
}