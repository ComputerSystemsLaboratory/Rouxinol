#include <iostream>
#include <cstdio>
using namespace std;

int A[100000];

int partition(int p, int r)
{
	int x = A[r];//???????????????????????????????´????
	int i = p - 1;//x??\??????????´????????°??????????
	
	//j???x????????§??????????´????????°??????????
	for (int j = p; j < r; j++) {
		if (A[j] <= x) {
			i++;
			swap(A[j], A[i]);
		}
	}
	swap(A[i + 1], A[r]);
	return i + 1;
}

int main()
{
	int n;
	scanf("%d", &n);
	for (int i = 0; i < n; i++) {
		scanf("%d", &A[i]);
	}
	int r = partition(0, n - 1);

	for (int i = 0; i < n; i++) {
		if (i) printf(" ");
		if (i == r) printf("[%d]", A[i]);
		else printf("%d", A[i]);
	}
	printf("\n");
	return 0;
}